import com.shatteredpixel.shatteredpixeldungeon.actors.buffs.Healing;
import com.shatteredpixel.shatteredpixeldungeon.actors.hero.Hero;
import com.shatteredpixel.shatteredpixeldungeon.messages.Messages;
import com.shatteredpixel.shatteredpixeldungeon.sprites.ItemSpriteSheet;
import com.shatteredpixel.shatteredpixeldungeon.utils.GLog;

public class PotionOfNaturalHealing extends Potion {

	{
		icon = ItemSpriteSheet.Icons.POTION_HEALING;

		bones = true;
	}
	
	@Override
	public void apply(Hero hero) {
		identify();
		
		// Heals the hero slowly over time instead of immediate healing
		Buff.affect(hero, Healing.class).setHeal(hero.HT/2, 0.5f, 0);
		
		// Also provides a temporary blessing
		Buff.affect(hero, Bless.class, 30f);
		
		GLog.p(Messages.get(this, "heal"));
		
		Badges.validateItemsIdentified();
	}
	
	@Override
	public int value() {
		return isKnown() ? 30 * quantity : super.value();
	}
} 